package bucles;
/*
 * programa de la tabla de multiplicar con for
 * programa para aprender a utilizar el for
 * 06/10/2025 Eder Gracia
 */
public class BucleFor {

	public static void main(String[] args) {
		//declarar variables
		int numero=7;
		
		//estructura
		for (int i = 1; i <=10; i++) {
			System.out.println(numero+ " multiplicado por " + i+ " es igual a "+ numero*i);	
		}
	}

}
